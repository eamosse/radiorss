package fr.unice.i3s.wimmics.rss;

public class Source {

	/**
	 * textValue
	 */
	public String source;
	/**
	 * attribute
	 */
	public String url;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}
}
